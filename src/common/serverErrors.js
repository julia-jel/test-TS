import _ from 'lodash';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const commonMessageMap = {
    ['network_error']: 'Сервер не отвечает',
    ['insufficient_privileges']: 'Недостаточно прав',
    ['already_exists']: 'Уже существует.'
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const messageMap = {
    ['already_exists']: 'Уже существует.',
    ['not_found']: 'Не найдено.',
    ['invalid_user']: 'Неверный пользователь.',
    ['invalid_parameter']: 'Неверный параметр.'
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function getErrorText(error) {
    const code = _.get(error, 'code');
    if(!code) return '';

    return messageMap[code] || 'Ошибка сервера.';
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function getCommonErrorText(error) {
    const code = _.get(error, 'code');
    if(!code) return '';

    return commonMessageMap[code] || 'Ошибка сервера.';
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export {
    getErrorText,
    getCommonErrorText
};

