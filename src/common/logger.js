////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @see https://github.com/flatiron/winston/

import winston from 'winston';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const transports = [
    new (winston.transports.Console)({
        json: false,
        timestamp: true,
        handleExceptions: false,
        colorize: true,
        level: 'silly'
    })
];

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const logger = new (winston.Logger)({
    transports,
    exitOnError: true
});

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
export default logger;

